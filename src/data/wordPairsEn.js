/** Traduit un mot de carte selon la langue (fallback : texte français). */
export function localizeWord(word, lang) {
  if (lang !== 'en' || !word) return word
  return WORD_EN[word] || word
}

/** Traduit un titre / univers selon la langue (fallback : texte français). */
export function localizeFrom(from, lang) {
  if (lang !== 'en' || !from) return from
  return FROM_EN[from] || from
}

/** Traduit un indice « lien » selon la langue (fallback : texte français). */
